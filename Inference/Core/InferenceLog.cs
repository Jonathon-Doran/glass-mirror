using System;

namespace Inference.Core;

///////////////////////////////////////////////////////////////////////////////////////////////
// InferenceLog
//
// Static logging class for inference-specific messages. These are messages relating
// directly to opcode identification decisions and signals, separate from general
// debug logging which goes through Glass.Core.DebugLog.
///////////////////////////////////////////////////////////////////////////////////////////////
public static class InferenceLog
{
    private static volatile Action<string>? _log;

    ///////////////////////////////////////////////////////////////////////////////////////////
    // Initialize
    //
    // Wires up the inference log output action.
    //
    // log:  Action that writes a message to the inference log UI.
    ///////////////////////////////////////////////////////////////////////////////////////////
    public static void Initialize(Action<string> log)
    {
        _log = log;
    }

    ///////////////////////////////////////////////////////////////////////////////////////////
    // Shutdown
    //
    // Disconnects the inference log output.
    ///////////////////////////////////////////////////////////////////////////////////////////
    public static void Shutdown()
    {
        _log = null;
    }

    ///////////////////////////////////////////////////////////////////////////////////////////
    // Write
    //
    // Writes a timestamped message to the inference log.
    //
    // message:  The message to write.
    ///////////////////////////////////////////////////////////////////////////////////////////
    public static void Write(string message)
    {
        string timestamped = DateTime.Now.ToString("HH:mm:ss.fff") + "  " + message;
        _log?.Invoke(timestamped);
    }
}