namespace Inference.Models;

///////////////////////////////////////////////////////////////////////////////////////////////
// AnalysisCandidate
//
// Represents a candidate identification for an unknown opcode.
// Displayed in the CandidateGrid on the Analysis tab.
///////////////////////////////////////////////////////////////////////////////////////////////
public class AnalysisCandidate
{
    ///////////////////////////////////////////////////////////////////////////////////////////////
    // Name
    //
    // The proposed logical name for this opcode (e.g., "OP_PlayerProfile").
    ///////////////////////////////////////////////////////////////////////////////////////////////
    public string Name { get; set; } = "";

    ///////////////////////////////////////////////////////////////////////////////////////////////
    // Confidence
    //
    // A human-readable confidence level (e.g., "High", "Medium", "72%").
    ///////////////////////////////////////////////////////////////////////////////////////////////
    public string Confidence { get; set; } = "";

    ///////////////////////////////////////////////////////////////////////////////////////////////
    // Evidence
    //
    // Summary of the evidence supporting this identification.
    ///////////////////////////////////////////////////////////////////////////////////////////////
    public string Evidence { get; set; } = "";
}
