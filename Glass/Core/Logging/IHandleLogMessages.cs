using System;

namespace Glass.Core.Logging;

///////////////////////////////////////////////////////////////////////////////////////////////
// IHandleLogMessages
//
// Interface for objects that receive log messages from the DebugLog system.
// Implementations handle writing messages to a specific destination (file,
// UI control, etc.) and cleaning up their resources on shutdown.
//
// Implementations are registered with DebugLog via AddHandler and are
// called for every message routed to their sink.
///////////////////////////////////////////////////////////////////////////////////////////////
public interface IHandleLogMessages
{
    ///////////////////////////////////////////////////////////////////////////////////////////////
    // Write
    //
    // Writes a single log message to this handler's destination.
    //
    // message:  The message text to write
    ///////////////////////////////////////////////////////////////////////////////////////////////
    void Write(string message);

    ///////////////////////////////////////////////////////////////////////////////////////////////
    // Shutdown
    //
    // Flushes and releases all resources held by this handler.  Called by
    // DebugLog.Shutdown().  No further Write calls will be made after this.
    ///////////////////////////////////////////////////////////////////////////////////////////////
    void Shutdown();
}