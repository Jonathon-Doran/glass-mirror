namespace Glass.Input;

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// HidAxisEventArgs
//
// Arguments for an analog axis state change event from a HID device.
// Device identifies the specific physical device instance.
//
// AxisName identifies the axis e.g. "JoystickX", "JoystickY".
// Value is the current axis value 0-255, center at 127.
// PreviousValue is the previous axis value, useful for delta calculation.
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
public class HidAxisEventArgs : EventArgs
{
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    // Device
    //
    // The specific device instance that fired this event.
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    public HidDeviceInstance? Device { get; set; }

    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    // AxisName
    //
    // The axis identifier e.g. "JoystickX", "JoystickY".
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    public string AxisName { get; }

    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    // Value
    //
    // The current axis value. Range 0-255, center at 127.
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    public byte Value { get; }

    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    // PreviousValue
    //
    // The previous axis value. Useful for calculating movement delta.
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    public byte PreviousValue { get; }

    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    // HidAxisEventArgs
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    public HidAxisEventArgs(string axisName, byte value, byte previousValue)
    {
        AxisName = axisName;
        Value = value;
        PreviousValue = previousValue;
    }
}