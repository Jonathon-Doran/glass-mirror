using Glass.Data.Models;

namespace Glass.Input;

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// IParseHidReport
//
// Parses raw HID report bytes from a specific device type into key state changes.
// One implementation exists per supported device type.
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
public interface IParseHidReport
{
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    // Device
    //
    // The device type this parser handles.
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    KeyboardType Device { get; }

    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    // Parse
    //
    // Parses a raw HID report and returns the list of key state changes detected.
    // Returns an empty list if no key state changes are present.
    //
    // report:  The raw report bytes from the device
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    IReadOnlyList<HidKeyEventArgs> Parse(byte[] report);
}