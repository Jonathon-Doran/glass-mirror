namespace Glass.Input;

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// IParseHidAxes
//
// Implemented by parsers that support analog axis input.
// Separate from IParseHidReport so devices without analog input
// are not required to implement it.
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
public interface IParseHidAxes
{
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    // ParseAxes
    //
    // Parses a raw HID report and returns any analog axis state changes detected.
    // Returns an empty list if no axis changes are present.
    //
    // report:  The raw report bytes from the device
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    IReadOnlyList<HidAxisEventArgs> ParseAxes(byte[] report);
}
