namespace Glass.Controls;

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// KeyDisplay
//
// Represents the display state of a single key in the KeyboardLayoutControl.
// Populated by the caller — the control has no knowledge of bindings or pages.
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
public class KeyDisplay
{
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    // KeyName
    //
    // The physical key identifier, e.g. "G1", "X-14". Always set.
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    public string KeyName { get; set; } = string.Empty;

    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    // Label
    //
    // The command label assigned to this key. Empty if unbound.
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    public string Label { get; set; } = string.Empty;

    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    // KeyType
    //
    // Whether this key behaves as a momentary press or a toggle.
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    public KeyType KeyType { get; set; } = KeyType.Momentary;

    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    // IsSelected
    //
    // Whether this key is currently selected in the editor.
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    public bool IsSelected { get; set; }

    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    // IsPressed
    //
    // For momentary keys: whether the key is currently held down.
    // For toggle keys: whether the toggle is currently on.
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    public bool IsPressed { get; set; }
}
