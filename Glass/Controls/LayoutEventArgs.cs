using System.Windows;

namespace Glass.Controls;

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// LayoutEventArgs
//
// Event arguments for key events in the KeyboardLayoutControl.
// Carries the key name and pressed state of the activated key.
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
public class LayoutEventArgs : RoutedEventArgs
{
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    // KeyName
    //
    // The name of the key that was activated.
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    public string KeyName { get; }

    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    // IsPressed
    //
    // The pressed state of the key.
    // For momentary keys: true on press, false on release.
    // For toggle keys: true when toggled on, false when toggled off.
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    public bool IsPressed { get; }

    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    // LayoutEventArgs
    //////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    public LayoutEventArgs(RoutedEvent routedEvent, string keyName, bool isPressed) : base(routedEvent)
    {
        KeyName = keyName;
        IsPressed = isPressed;
    }
}

//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// LayoutEventHandler
//
// Delegate for all key events in the KeyboardLayoutControl.
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
public delegate void LayoutEventHandler(object sender, LayoutEventArgs e);